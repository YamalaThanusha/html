# writing arithmetic operations 
a=8
b=2
print("sum:",a+b)
print("subtraction:",a-b)
print("multiplication:",a*b)
print("division:",a/b)
print("floor division:",a//b)
print("modulo:",a%b)
print("power:",a**b)

#assignment operators
a=2
b=3
a *=b  #a=a*b
print(a)

#relational operators
a=100
b=99
print(a==b)   #equal to operator
print(a!=b)   #not equal to operator
print(a<=b)   #less than or equal to operator
print(a>=b)   #greater than or equal to operator 
print(a>b)    #greater than operator
print(a<b)    #less than operator

#logical operators
thanusha=True
sasi=False
print(thanusha and sasi) #here using and(multiplication) operator
print(thanusha  or sasi) #here using or(addition) operator 
print(not thanusha)      #here using not(opposite)operator























